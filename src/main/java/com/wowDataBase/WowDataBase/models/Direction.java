package com.wowDataBase.WowDataBase.models;

import jakarta.persistence.*;

@Entity
@Table(name = "directions")
public class Direction {
    @Id
    @GeneratedValue
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
